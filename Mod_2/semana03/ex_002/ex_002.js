// FUNCÃO: É um bloco de código que realiza uma tarefa específica e pode ser utilizado incontáveis vezes ao longo do projeto. A funcão pode receber entradas, denominadas parâmetros, que são valores reservados para a execucão da funcão. 

function somar (a, b){
    return a + b;
}
console.log("O resultado da soma 4+3 é " + somar(4,3) + ".")


function quadrado(numero){
    return numero * numero;
}
console.log("Três ao quadrado é " + quadrado(3) + ".")


function divisao(a, b){
    return a / b;
}
console.log("20 dividido por 5 é " + divisao(20, 5) + ".")