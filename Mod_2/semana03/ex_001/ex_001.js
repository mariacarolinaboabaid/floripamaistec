// STRING: representa uma sequência de caracteres, como palavras ou frases
var nome = "Maria Carolina"; 

// NUMBER: representa valores numéricos, sejam eles inteiros ou de ponto flutuante
var idade = 25;

// BOOLEAN: representa o valor de verdadeiro ou falso
var boolean = true; 

// ARRAY: representa uma coleção de elementos/valores, sejam eles do mesmo tipo ou não
var numeros = [1, 2, 3, 4, 5]; 

// OBJETO: representa uma coleção, no qual cada elemento é constituído por uma propriedade e um valor correspondente
var objeto = { nome: "Maria Carolina", idade: 25, feliz: true }; 

//NULO: representa a ausência de valor
var nulo = null; 
